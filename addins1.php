#
# Небольшие коментарии по поводу конвертации переменных на примере строка/цифра
#


$a = "3.14";
$b = 15;
var_dump($a + $b);  # Математический используя знак + сложить строку и цифру получится как (3.14 + 15)


$a = "3.14";
$b = 15;
var_dump($a . $b); # Строковое сложение - например "Привет "."мир!"   Будет как "Привет мир!"


$a = "текст перед 3.14";
$b = 15;
var_dump($a + $b); # Цифровое сложение - тут сконвертировал текст в цифру - неудачно и $a принял как 0


$a = "3.14 текст после";
