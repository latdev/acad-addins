#
# Теперь об операторах сравнения!
#

$a = "3.14";
$b = 3.14;

var_dump($a == $b); # Вернёт истину - потому-что в принцепе значения похожи (типы строка/цифра не проверялись)
var_dump($a === $b); # Вернёт лож - потому что значения не похожи и по типу и по значению
var_dump($a === "3.14"); # Вернёт истину - что значения похожи и по типу и по значению
